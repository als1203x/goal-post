import UIKit

extension UIButton  {
    
    func setSelectorColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.8115362525, blue: 0.2999568582, alpha: 1)
    }
    
    func setDeselectedColor()   {
        self.backgroundColor = #colorLiteral(red: 0.7445629835, green: 0.8871654272, blue: 0.7408195138, alpha: 1)
    }
}
